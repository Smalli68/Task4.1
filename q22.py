# Example: A list representing rows
table_rows = [
    ["Name", "Age"],
    ["Alice", 24],
    ["Bob", 19]
]
num_rows = len(table_rows)
print(f"Number of rows: {num_rows}")

# Example: A list representing columns
table_columns = [
    ["Name", "Alice", "Bob"],
    ["Age", 24, 19]
]
num_columns = len(table_columns)
print(f"Number of columns: {num_columns}")