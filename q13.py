# Create a tuple with numbers
my_numbers_tuple = (10, 20, 30, 40, 50)

# Print the entire tuple
print("The entire tuple:", my_numbers_tuple)

# Access and print a specific item from the tuple using its index
# Tuples are zero-indexed, so the first item is at index 0, the second at index 1, and so on.
print("The first item (index 0):", my_numbers_tuple[0])
print("The third item (index 2):", my_numbers_tuple[2])