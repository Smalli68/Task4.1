# Define a list
my_list = [1, 2, 3, "apple", "banana", 4.5]

print(f"Original list: {my_list}")
print(f"Type of original list: {type(my_list)}")

# Convert the list to a tuple using the tuple() constructor
my_tuple = tuple(my_list)

print(f"\nConverted tuple: {my_tuple}")
print(f"Type of converted tuple: {type(my_tuple)}")